package com.web.pojo;

import java.util.Date;

/**
 * 通报批评
 * 
 * @author wqg
 * 
 *         2013-10-28下午3:27:42
 */
public class Reprimand extends BasePojo
{
	private String loginname;// 通报批评人

	private long dwid;// 批评的单位

	private String content;// 批评的内容

	private Date time;// 通报批评时间

	private long flag;// 标注是否已经倒查

	private long type;// 标注批评的类型

	private long driverid;// 驾驶员id

	private long pid;

	private Driver driver;// 指向driver的引用

	private Company company;// 指向company的引用

	public long getPid()
	{
		return pid;
	}

	public void setPid(long pid)
	{
		this.pid = pid;
	}

	public Company getCompany()
	{
		return company;
	}

	public void setCompany(Company company)
	{
		this.company = company;
	}

	public Driver getDriver()
	{
		return driver;
	}

	public void setDriver(Driver driver)
	{
		this.driver = driver;
	}

	public long getType()
	{
		return type;
	}

	public void setType(long type)
	{
		this.type = type;
	}

	public long getDriverid()
	{
		return driverid;
	}

	public void setDriverid(long driverid)
	{
		this.driverid = driverid;
	}

	public String getLoginname()
	{
		return loginname;
	}

	public void setLoginname(String loginname)
	{
		this.loginname = loginname;
	}

	public Date getTime()
	{
		return time;
	}

	public void setTime(Date time)
	{
		this.time = time;
	}

	public long getDwid()
	{
		return dwid;
	}

	public void setDwid(long dwid)
	{
		this.dwid = dwid;
	}

	public String getContent()
	{
		return content;
	}

	public void setContent(String content)
	{
		this.content = content;
	}

	public long getFlag()
	{
		return flag;
	}

	public void setFlag(long flag)
	{
		this.flag = flag;
	}
}
