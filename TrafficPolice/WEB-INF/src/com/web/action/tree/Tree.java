package com.web.action.tree;

import java.util.ArrayList;
import java.util.List;

/**
 * 
 * @author fossil
 * @create 2013-5-6 下午01:31:14
 */
public class Tree {
	private String id = "";// 本节点id
	private String name = "";// 本节点显示名称
	private List<Tree> childList = new ArrayList<Tree>();// 儿子

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<Tree> getChildList() {
		return childList;
	}

	public void setChildList(List<Tree> childList) {
		this.childList = childList;
	}

}
